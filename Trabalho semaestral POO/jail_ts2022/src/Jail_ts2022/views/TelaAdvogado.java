    private void btn_salvarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btn_salvarKeyPressed
        if (f.validarEmail(txt_email.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Email Digitado Inválido! Por Favor Introduza um email válido", "Atenção", JOptionPane.WARNING_MESSAGE);
            btn_salvar.setEnabled(false);
        } else if (tf_contactoAdvogado.getText().length() < 16) {
            btn_salvar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Os Campos Contactos Devem Ser Preenchidos \n O número de Telefone Deve conter apenas 9 digito!!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (txt_NomeAdvogado.getText().isEmpty()) {
            btn_salvar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo Nome do Advogado deve Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (tf_contactoAdvogado.getText().isEmpty()) {
            btn_salvar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo Telefone de Advogado Deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_salvarKeyPressed

