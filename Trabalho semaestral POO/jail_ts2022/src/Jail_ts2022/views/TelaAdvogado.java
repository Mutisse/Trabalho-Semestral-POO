 void listarAdvogado() {

        for (int i = 0; i < listarAdvogados.size(); i++) {
            cb_ListarAdvogados.addItem(listarAdvogados.get(i));

        }

    }

    void registrarAdvogado() {
        if (jC_selecionar.isSelected()) {

            recluso.setAdvogado((Advogado) cb_ListarAdvogados.getSelectedItem());
            advogadoDAo.tbrecluso_has_tbadvogados(recluso);

        } else if (jC_registrar.isSelected()) {
            advogado.setId_Advogado(lbl_idAdvogado.getText());
            recluso.setAdvogado(advogado);
            //
            advogado.setNomeDeAdvogado(txt_NomeAdvogado.getText());
            recluso.setAdvogado(advogado);
            //
            advogado.setEmailAdvogado(txt_email.getText());
            recluso.setAdvogado(advogado);
            //
            advogado.setContactoDeAdvogado(tf_contactoAdvogado.getText());
            recluso.setAdvogado(advogado);
            // salvar advogado
            advogadoDAo.regitrarAdvogado(recluso);
            advogadoDAo.tbrecluso_has_tbadvogados(recluso);
        }

    }

