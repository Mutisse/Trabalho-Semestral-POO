    void validarVcampos() {

        if (txt_Nome.getText().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo  Nome deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (txt_Nome.getText().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo Apelido Deve  Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (txt_NumeroDeBI.getText().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo Número de BI Deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (cb_provincias.getActionCommand().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Não foi seleccionada nenhuma Categoria!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (cb_crimes.getActionCommand().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Não foi seleccionado nenhum crime, por favor Informe um crime.", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (cb_EstadoDePrisao.getActionCommand().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Não foi seleccionado nenhum estado, por favor Informe  um estado do prisioneiro.", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (cb_celas.getActionCommand().isEmpty()) {
            btn_proximo.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Por favor informe uma cela ao prisioneiro.", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            btn_proximo.setEnabled(true);
            table1Registrar();
        }
    }


