    public void table1Registrar() {
        try {

            int contt = 0;
            for (int i = 0; i < tb_recluso.getRowCount(); i++) {
                if (txt_Nome.getText().equals(tb_recluso.getModel().getValueAt(i, 1)) && txt_Nome.getText().equals(tb_recluso.getModel().getValueAt(i, 2))) {
                    contt = 1;
                }
            }

            if (contt >= 1) {
                int i = 0;
                i = JOptionPane.showConfirmDialog(null, "Este Recluso esteve preso. Dseja recuperar os dados??", "Notificação ", JOptionPane.YES_NO_OPTION);
                if (i == JOptionPane.YES_OPTION) {
                    System.out.println(" Preencher os dados ");
                }
            } else {

                recluso.setId_registro(lbl_idregistro.getText());
                recluso.setCodigo(lbl_Codigo.getText());
                recluso.setApelido(txt_Apelido.getText());
                recluso.setNome(txt_Nome.getText());
                recluso.setNumeroDeBI(txt_NumeroDeBI.getText());

                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                String anoStr = formato.format(JDataFormatada.getDate());
                recluso.setDatadeNascimento(anoStr);

                if (rb_Masculino.isSelected()) {
                    recluso.setGenero(rb_Masculino.getActionCommand());
                } else if (rb_Feminino.isSelected()) {
                    recluso.setGenero(rb_Feminino.getActionCommand());
                }
                recluso.setRegistroDeBens(txtA_bens.getText());
                recluso.setNivelAcademico(cb_nivelAcadademico.getSelectedItem().toString());

                recluso.setDoenca(cb_doencas.getSelectedItem().toString());
                recluso.setEstadoDaPrisao(cb_EstadoDePrisao.getSelectedItem().toString());
                // salvar
                ArrayList<Crimes> lista = new ArrayList<>();
                for (int i = 0; i < modelListCrimes.size(); i++) {
                    lista.add((Crimes) modelListCrimes.get(i));
                }
                recluso.setCrime(lista);
                // crime.setCategoriaDeCrime(String.valueOf(crime));
                recluso.setDuracaoDapena(String.valueOf(lbl_penaDefinida.getText()));
                reclusaoDAo.saveRecluso(recluso);
                /*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ registrar endreco ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
                celaDAo.ocuparAcela(recluso);
                crimesDAo.tbrecluso_has_tbcrimes(recluso);
                // endreco
                endereco.setBairro(txt_bairro.getText());
                recluso.setEndereco(endereco);
                endereco.setRua(txt_rua.getText());
                recluso.setEndereco(endereco);
                endereco.setCasa(txt_nCasa.getText());
                recluso.setEndereco(endereco);
                endereco.setQuarteirao(txt_quarteirao.getText());
                recluso.setEndereco(endereco);
                recluso.setId_registro(lbl_idregistro.getText());
                //provincia;
                endereco.setProvincias((Provincias) cb_provincias.getSelectedItem());
                recluso.setEndereco(endereco);
                // endereco salvar 
                enderecoDAo.regitrarEndereco(recluso);
                int i = JOptionPane.showConfirmDialog(null, " vai registar advogado?", "", JOptionPane.YES_NO_OPTION);

                if (i == JOptionPane.YES_OPTION) {
                    new TelaAdvogado().setVisible(true);
                }
                LimparCampos();
                PrencherTabela();

            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e + " Erro problema na digitação dos dados ", "Notificação", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        }

    }

