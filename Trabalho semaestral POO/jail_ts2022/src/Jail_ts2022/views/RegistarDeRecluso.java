    void removeTojlist() {

        try {
            cb_crimes.addItem(jList_crimes.getSelectedValue());
            Crimes removido = (Crimes) jList_crimes.getSelectedValue();
            localDate = localDate.minusMonths(Long.parseLong(removido.getDuracaoEmMeses()));
            localDate = localDate.minusYears(Long.parseLong(removido.getDuracaoEmAno()));
            lbl_penaDefinida.setText(localDate + "");
            modelListCrimes.removeElement(jList_crimes.getSelectedValue());
            lbl_duracaoEmMeses.setText(removido.getDuracaoEmMeses());
            lbl_duracaoEmAno.setText(removido.getDuracaoEmAno());
            lbl_duracaoEmMeses.setText(null);
            lbl_duracaoEmAno.setText(null);
            JOptionPane.showMessageDialog(null, "O crime " + removido.getCategoriaDeCrime() + " foi retirado da lista", "", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione um crime para retirar da lista", "", JOptionPane.WARNING_MESSAGE);
        }

    }

