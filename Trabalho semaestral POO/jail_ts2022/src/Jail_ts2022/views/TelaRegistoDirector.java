    void preencherPerfil() {
        NivelDeAcessoDao nivel = new NivelDeAcessoDao();
        List<NivelDeAcesso> niveldeAcesso = nivel.ListarNivelDeAcesso();

        for (int i = 0; i < niveldeAcesso.size(); i++) {
            if (niveldeAcesso.get(i).getCategoria().equalsIgnoreCase("Director geral")) {
                cb_perfil.addItem(niveldeAcesso.get(i));

            }

        }
    }

    void validacaoDosCampos() {
        if (txt_Nome.getText().isEmpty()) {
            btnRegistar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo  Nome deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (txt_Apelido.getText().isEmpty()) {
            btnRegistar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "O Campo Apelido Deve  Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else if (txt_email.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O Campo e-mail Deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
            btnRegistar.setEnabled(false);
        } else if (pf_definirAsenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O Campo senha Deve Ser Preenchido!", "Atenção", JOptionPane.WARNING_MESSAGE);
            btnRegistar.setEnabled(false);
        } else if (rb_Masculino.getActionCommand().isEmpty() || rb_Femenino.getActionCommand().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não foi seleccionada nenhuma Categoria!", "Atenção", JOptionPane.WARNING_MESSAGE);

            btnRegistar.setEnabled(false);
        } else {
            btnRegistar.setEnabled(true);
            registrar();
        }
    }

    void registrar() {

        try {
            if (txt_Apelido.getText().isEmpty() || txt_Nome.getText().isEmpty() || (rb_Masculino.getActionCommand().isEmpty() && rb_Masculino.getActionCommand().isEmpty()) || txt_email.getText().isEmpty() || pf_ConfirmarAsenha.getText().isEmpty() || pf_definirAsenha.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Preencha todos campos obrigatorios");
            } else {
                if (pf_definirAsenha.getText().length() > 3 || pf_ConfirmarAsenha.getText().length() > 3) {

                    director.setSenha(pf_definirAsenha.getText());
                    director.setApelido(txt_Apelido.getText());
                    director.setCodigo(lbl_Codigo.getText());
                    director.setNome(txt_Nome.getText());
                    if (rb_Masculino.isSelected()) {
                        director.setGenero(rb_Masculino.getActionCommand());
                    } else if (rb_Femenino.isSelected()) {
                        director.setGenero(rb_Femenino.getActionCommand());
                    }

                    director.setEmail(txt_email.getText());
                    director.setNivelDeAcesso((NivelDeAcesso) cb_perfil.getSelectedItem());
                    director.setProvincia((Provincias) cb_provincias.getSelectedItem());

                    directorDao.savarDirector(director);
                    limparCampos();
                    this.dispose();
                    new TelaLogin().setVisible(true);

                } else {

                    JOptionPane.showMessageDialog(null, "Senha inválida.\nCombine pelo menos 4 caracteres!", "Notificacao", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + " Erro problema na digitação dos dados ", "Notificação", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }
