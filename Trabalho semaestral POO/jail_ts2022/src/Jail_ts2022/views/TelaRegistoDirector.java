    private void pf_ConfirmarAsenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pf_ConfirmarAsenhaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (pf_definirAsenha.getText().equalsIgnoreCase(pf_ConfirmarAsenha.getText())) {
                pf_definirAsenha.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.GREEN));
                pf_ConfirmarAsenha.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.GREEN));
                btnRegistar.setEnabled(true);
                rb_Masculino.requestFocus();

            } else {
                pf_ConfirmarAsenha.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.red));
                btnRegistar.setEnabled(false);

            }

        } else if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            pf_ConfirmarAsenha.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.BLACK));
        }
    }//GEN-LAST:event_pf_ConfirmarAsenhaKeyPressed

